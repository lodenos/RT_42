#include "libft.h"

char	*ft_strdup(char const *s)
{
	char	*tmp;

	tmp = NULL;
	if (!s || !(tmp = (char*)malloc(sizeof(tmp) * (ft_strlen(s) + 1))))
		return (tmp);
	return (tmp = ft_strcpy(tmp, s));
}
